"""
Chain 2 — 内容分类 + 作者意图分析
====================================
输入：帖子内容 + 作者基本信息（角色、专业背景）
输出：内容类型 + 具体主题 + 作者意图

内容类型（content_type）枚举：
  财经分析   — 包含推理逻辑的分析性内容（宏观/市场/地缘/政策/技术），见下方子分类
  市场动向   — 纯描述性回顾，无推理或结论（"本周美股跌了X%"）
  产业链研究 — 对特定行业供应链、竞争格局的深度结构研究
  公司调研   — 聚焦单一或少数公司的基本面、业绩分析
  技术论文   — 学术性/方法论性深度研究，含量化模型或学术引用
  政策解读   — 对已公布政策（政府/央行/军事）的含义、影响分析

财经分析子分类（content_subtype）：
  市场分析 — 资产价格、交易机会、资产配置的逻辑推演
  地缘分析 — 地缘政治、国际关系、战争/冲突对经济的影响
  政策分析 — 财政/货币/监管政策的逻辑推演与市场影响（注意：非官方原文，而是分析）
  技术影响 — AI/科技趋势对产业/经济/市场的影响分析
  混合分析 — 同时涵盖以上多个维度，无法归入单一子类

作者意图（author_intent）枚举：
  传递信息   — 客观报道，让读者了解事实或现状
  影响观点   — 试图改变读者的认知或判断
  警示风险   — 突出特定风险，劝读者保持警惕
  推荐行动   — 给出具体投资/行动建议
  教育科普   — 帮助读者学习、理解某一领域
  引发讨论   — 抛出议题，邀请读者思考或辩论
  推广宣传   — 推广自身产品、书籍、服务或观点体系
  政治动员   — 政客/政治人物塑造胜利叙事、巩固支持者情绪、争取选票或政治合法性
"""

from __future__ import annotations

SYSTEM = """\
你是一名专业的财经内容分析师，擅长快速判断财经文章的内容类型、作者意图和来源机构。

【内容类型定义】
  财经分析   — 包含推理逻辑的分析性内容：宏观经济、市场走势、地缘政治、政策影响、技术趋势等
               → 核心特征：有论据 → 有推理 → 有结论
               → 达利欧的大周期、付鹏的宏观分析、机构研报，均归此类
  市场动向   — 纯描述性回顾，无推理或判断（"本周美股跌了X%，原因是..."）
               → 若文章仅陈述事实、无逻辑推演，才归此类
  产业链研究 — 对某一行业进行深度结构性分析：产业链分布、各环节主要玩家、竞争格局与护城河
               → 要求系统覆盖产业结构，落脚点是「产业本身长什么样」
  公司调研   — 聚焦单一或少数公司的基本面、业绩、竞争地位
  技术论文   — 学术性或方法论性深度研究，含量化模型或学术引用
  政策解读   — 对政府/央行/军事等已公布政策的含义、影响进行二次分析
               → 不论来源是官媒还是独立分析师，只要是在解读政策 → 政策解读

【财经分析子分类（content_subtype）】
  仅当 content_type = 财经分析 时填写，否则填 null。
  市场分析 — 主要围绕资产价格、交易逻辑、资产配置
  地缘分析 — 主要围绕地缘政治、国际关系、战争/冲突对经济的影响
  政策分析 — 主要围绕财政/货币/监管政策的逻辑推演（非原文宣布）
  技术影响 — 主要围绕 AI/科技趋势对产业/经济/市场的影响
  混合分析 — 同时涵盖多个维度，无法归入单一子类（宏观+地缘+政策混合）

【判断规则】
  一篇文章可以兼具多种类型，主分类（content_type）选最核心的一个，
  次分类（content_type_secondary）选第二重要的，没有则填 null。

  【财经分析 vs 市场动向 的关键区分：有无推理逻辑】
  财经分析 — 文章有「论据 → 推理 → 结论」的逻辑链条，即使叙述了大量历史
  市场动向 — 文章只陈述「发生了什么」，无作者判断或预测
  ⚠️ 历史叙事 ≠ 教育科普：若历史是论据、结论是当下市场判断 → 财经分析

  【产业链研究 vs 财经分析 的关键区分：落脚点】
  产业链研究 — 落脚点是「产业本身」：系统覆盖产业链结构和竞争格局
  财经分析   — 落脚点是「市场/投资含义」：通过产业/宏观趋势得出配置结论

  【政策解读 vs 财经分析·政策分析 的区分】
  政策解读   — 文章主体是解读一份具体政策文件（某一次央行决议、某个财政预算案）
  财经分析·政策分析 — 文章从政策角度分析宏观趋势，但不是针对单一政策文件

【作者意图】
  只回答一件事：作者希望读者看完之后去做什么？
  用一句动宾结构表达，≤20字。

  示例：
    · 警惕私募信贷的系统性风险
    · 重新审视AI基建的投资逻辑
    · 购买作者的书/课程
    · 加入付费社群
    · 支持某项政策/候选人
    · 了解某个行业的发展现状

【实际发言人】
  real_author_name — 根据标题和内容判断谁是真正的分析/发言人：
    · 若标题明确点名某人（如"邢自强最新分析""付鹏谈xxx""专访XXX"），该人即实际发言人，填其姓名
    · 若发布者是个人品牌账号/频道（如"付鹏的财经世界"），提取真实发言人姓名（如"付鹏"）
    · 若频道是转载/解读账号（如"解读君""财经频道"），必须从标题/内容中找出被分析/被采访的实际发言人
    · 若确实无法判断具体发言人（例如匿名机构报告、无特定发言人的新闻），填 null

【发文机关（仅政策解读类内容填写）】
  issuing_authority — 发文机关全称（如"国务院""全国人民代表大会"）
  authority_level   — 顶层设计（国务院/全国人大/中央）| 部委联合 | 部委独立
  非政策解读类内容，两字段均填 null。

输出合法 JSON，不加任何其他文字.\
"""


def build_user_message(
    content: str,
    author_name: str,
    author_role: str | None,
    author_expertise: str | None,
    content_summary: str | None,
    situation_note: str | None = None,
) -> str:
    summary_line = f"\n内容摘要（Chain1生成）：{content_summary}" if content_summary else ""
    situation_line = f"\n当前处境：{situation_note}" if situation_note else ""

    return f"""\
## 作者信息
姓名：{author_name}
职业角色：{author_role or "未知"}
专业背景：{author_expertise or "未知"}{situation_line}

## 帖子内容{summary_line}

{content[:2000]}{"..." if len(content) > 2000 else ""}

## 分析任务

1. 判断内容类型（主分类必填，次分类可选）
2. 若主分类为「财经分析」，判断子分类（市场分析|地缘分析|政策分析|技术影响|混合分析）；否则填 null
3. 提取文章标题：若文章有明确标题（如【】括起的标题、第一行独立的短标题），直接使用原始标题；否则用≤30字概括核心主题
4. 用一句动宾结构描述作者意图：读者看完后应该去做什么，≤20字
5. 从标题和内容判断实际发言人：标题点名某人 → 填该人姓名；转载/解读频道 → 找被采访/被分析的人；确实无法判断 → 填 null
6. 若内容类型为政策解读，识别发文机关全称和级别；否则填 null

请严格按以下 JSON 格式输出，不要输出任何其他内容：

```json
{{
  "content_type": "财经分析|市场动向|产业链研究|公司调研|技术论文|政策解读",
  "content_subtype": "市场分析|地缘分析|政策分析|技术影响|混合分析|null",
  "content_type_secondary": "同上主分类枚举或null",
  "content_topic": "文章原始标题（若有）或≤30字主题概括",
  "author_intent": "作者希望读者做什么（动宾结构，≤20字）",
  "real_author_name": "真实发言人姓名（个人品牌账号时填写）或null",
  "issuing_authority": "发文机关全称或null",
  "authority_level": "顶层设计|部委联合|部委独立或null"
}}
```
"""
