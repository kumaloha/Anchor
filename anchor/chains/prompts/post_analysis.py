"""
Chain 2 — 内容分类 + 作者意图分析
====================================
输入：帖子内容 + 作者基本信息（角色、专业背景）
输出：内容类型 + 具体主题 + 作者意图

内容类型（content_type）枚举：
  市场动向   — 对近期市场行情、资产价格变动的描述性回顾
  市场分析   — 对价格驱动逻辑、交易机会的分析性判断
  产业调研   — 对特定行业、赛道、供应链的深度研究
  公司调研   — 对单一或少数上市公司的基本面、业绩分析
  技术论文   — 学术性或技术性深度研究报告
  教育科普   — 以解释概念、历史、理论为主，传授知识
  政策宣布   — 政府、央行或监管机构的原始官方声明
  政策解读   — 对政策含义、影响、执行的二次分析

作者意图（author_intent）枚举：
  传递信息   — 客观报道，让读者了解事实或现状
  影响观点   — 试图改变读者的认知或判断
  警示风险   — 突出特定风险，劝读者保持警惕
  推荐行动   — 给出具体投资/行动建议
  教育科普   — 帮助读者学习、理解某一领域
  引发讨论   — 抛出议题，邀请读者思考或辩论
  推广宣传   — 推广自身产品、书籍、服务或观点体系
  政治动员   — 政客/政治人物塑造胜利叙事、巩固支持者情绪、争取选票或政治合法性
"""

from __future__ import annotations

SYSTEM = """\
你是一名专业的财经内容分析师，擅长快速判断财经文章的内容类型、作者意图和来源机构。

【内容类型定义】
  市场动向   — 描述性回顾近期市场行情、资产价格变动（"本周美股跌了X%，原因是..."）
  市场分析   — 分析价格背后的驱动逻辑、交易机会或资产配置含义
  产业调研   — 对某一行业/赛道进行深度结构性分析：产业链分布、各环节主要玩家、竞争格局与护城河
  公司调研   — 聚焦单一或少数公司的基本面、业绩、竞争地位
  技术论文   — 学术性或方法论性深度研究，含量化模型或学术引用
  教育科普   — 以传授知识为主，解释概念、历史规律、经济理论
  政策宣布   — 政府、央行、监管机构的原始官方声明（原文转载或摘录）
  政策解读   — 对已公布政策的含义、影响、执行效果进行二次分析

【判断规则】
  一篇文章可以兼具多种类型，主分类（content_type）选最核心的一个，
  次分类（content_type_secondary）选第二重要的，没有则填 null。

  【产业调研 vs 市场分析 的关键区分：分析深度与落脚点】
  产业调研 — 落脚点是「产业本身」：要求覆盖产业链分布、各环节主要玩家、各环节的不可替代性或护城河
    → 若缺少对产业结构和竞争格局的系统拆解，不应归为产业调研
  市场分析 — 落脚点是「市场/投资含义」：通过分析产业/宏观趋势，得出交易机会、资产配置或投资建议
    → 「AI发展对股市的影响」「产业趋势如何改变投资逻辑」→ 市场分析，而非产业调研
  ⚠️ 判断标准：主要结论是「这个产业长什么样」→ 产业调研；主要结论是「这对市场/投资意味着什么」→ 市场分析

  【政策宣布 vs 政策解读 的关键区分：来源身份】
  政策宣布 — 适用于官方发声装置（国家媒体/政府机构），无论文章是否含解释性语言：
    → 新华社、人民日报、央视/CCTV、中国政府网、各部委官网、官方英文媒体（Xinhua/ChinaDaily）
    → 这类来源发布的政策内容是"宣布"本身，而非对政策的独立评论
  政策解读 — 仅适用于第三方分析师、独立机构、学者、媒体评论员对已公布政策的二次分析
    → 区分标准：作者是否独立于政策制定/宣传体系？是 → 政策解读；否 → 政策宣布

【作者意图】
  只回答一件事：作者希望读者看完之后去做什么？
  用一句动宾结构表达，≤20字。

  示例：
    · 警惕私募信贷的系统性风险
    · 重新审视AI基建的投资逻辑
    · 购买作者的书/课程
    · 加入付费社群
    · 支持某项政策/候选人
    · 了解某个行业的发展现状

【实际发言人】
  real_author_name — 根据标题和内容判断谁是真正的分析/发言人：
    · 若标题明确点名某人（如"邢自强最新分析""付鹏谈xxx""专访XXX"），该人即实际发言人，填其姓名
    · 若发布者是个人品牌账号/频道（如"付鹏的财经世界"），提取真实发言人姓名（如"付鹏"）
    · 若频道是转载/解读账号（如"解读君""财经频道"），必须从标题/内容中找出被分析/被采访的实际发言人
    · 若确实无法判断具体发言人（例如匿名机构报告、无特定发言人的新闻），填 null

【发文机关（仅政策类内容填写）】
  issuing_authority — 发文机关全称（如"国务院""全国人民代表大会"）
  authority_level   — 顶层设计（国务院/全国人大/中央）| 部委联合 | 部委独立
  非政策宣布/政策解读类内容，两字段均填 null。

输出合法 JSON，不加任何其他文字。\
"""


def build_user_message(
    content: str,
    author_name: str,
    author_role: str | None,
    author_expertise: str | None,
    content_summary: str | None,
    situation_note: str | None = None,
) -> str:
    summary_line = f"\n内容摘要（Chain1生成）：{content_summary}" if content_summary else ""
    situation_line = f"\n当前处境：{situation_note}" if situation_note else ""

    return f"""\
## 作者信息
姓名：{author_name}
职业角色：{author_role or "未知"}
专业背景：{author_expertise or "未知"}{situation_line}

## 帖子内容{summary_line}

{content[:2000]}{"..." if len(content) > 2000 else ""}

## 分析任务

1. 判断内容类型（主分类必填，次分类可选）
2. 提取文章标题：若文章有明确标题（如【】括起的标题、第一行独立的短标题），直接使用原始标题；否则用≤30字概括核心主题
3. 用一句动宾结构描述作者意图：读者看完后应该去做什么，≤20字
4. 从标题和内容判断实际发言人：标题点名某人 → 填该人姓名；转载/解读频道 → 找被采访/被分析的人；确实无法判断 → 填 null
5. 若内容类型为政策宣布/政策解读，识别发文机关全称和级别；否则填 null

请严格按以下 JSON 格式输出，不要输出任何其他内容：

```json
{{
  "content_type": "市场动向|市场分析|产业调研|公司调研|技术论文|教育科普|政策宣布|政策解读",
  "content_type_secondary": "同上枚举或null",
  "content_topic": "文章原始标题（若有）或≤30字主题概括",
  "author_intent": "作者希望读者做什么（动宾结构，≤20字）",
  "real_author_name": "真实发言人姓名（个人品牌账号时填写）或null",
  "issuing_authority": "发文机关全称或null",
  "authority_level": "顶层设计|部委联合|部委独立或null"
}}
```
"""
