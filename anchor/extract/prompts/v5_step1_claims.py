"""
v5 Step 1 — 论证骨架提取
===========================
目标：提取论证的骨架——作者用来支撑核心主张的关键证据，
      以及由证据推导出的主要结论，识别两者之间的有向支撑关系。

提取哲学：
  - 提取论证的「承重墙」，不提取装饰性表述
  - 粒度对标「人工大纲」而非「逐句拆解」
  - 一篇文章/帖子通常 5–10 个节点足够
  - 中间推导步骤不作为独立节点（除非作者明确将其作为独立论点）

输出 JSON：
{
  "is_relevant_content": true,
  "skip_reason": null,
  "claims": [{"id": 0, "text": "...", "summary": "..."}],
  "edges": [{"from_id": 0, "to_id": 1}]
}
"""

from __future__ import annotations

SYSTEM = """\
你是一名专业的论证分析师。你的任务是从文章或帖子中提取论证的骨架：
作者用来支撑核心主张的关键证据（事实、数据、观察），以及由这些证据推导出的主要结论。

【提取原则】

原则 1 — 只提取「承重墙」
  每个节点必须对论证的最终结论有不可或缺的支撑作用。
  问自己：「如果删掉这个节点，作者的论证会垮吗？」
  若不会垮 → 不提取（它只是修辞或铺垫）。
  若会垮   → 提取。

原则 2 — 粒度对标「大纲」，不是「逐句」
  一篇 300-600 字的文章，目标提取 5–10 个节点。
  可以把几句话合并成一个节点，只要它们共同表达同一个论证单元。
  不要把一个完整论点的前半句和后半句拆成两个节点。

原则 3 — 区分「证据」和「结论」，而不要合并
  作者引用的可观察事实/数据（如"今年S&P500跑赢纳斯达克"）→ 单独成节点
  作者由此得出的判断（如"HALO交易崛起"）→ 单独成节点
  边方向：证据 → 结论

原则 4 — 不提取作者未明确说出的内容
  不要为了让逻辑更完整而补充隐含步骤，那是后续步骤的工作。

原则 5 — 边只连接有直接逻辑支撑关系的节点对
  「A 是 B 的原因」或「A 支撑 B」→ 建边 A→B
  「A 和 B 都支撑 C」→ 建边 A→C 和 B→C（不建 A→B）

输出合法 JSON，不加任何其他文字。\
"""


def build_user_message(
    content: str, platform: str, author: str, today: str,
    author_intent: str | None = None,
) -> str:
    intent_line = f"\n作者意图（预判）：{author_intent}" if author_intent else ""
    return f"""\
## 分析上下文
当前日期：{today}
平台：{platform}
作者：{author}{intent_line}

## 文章内容
{content}

## 提取任务

Step A — 相关性判断
  此内容是否包含可分析的论证（观点、事实依据或预测）？
  若否（广告/表情包/纯感情内容），设 is_relevant_content=false，其余字段留空。

Step B — 识别核心结论
  找到作者最终想让读者接受的核心主张。
  注意：金融/评论类文章常见「先抛结论、再给依据」的顶层结构，核心结论可能出现在
  开头、结尾或任何位置（常以「笔者认为/综合来看/在这个意义上/更重要的是」引导）。

Step C — 提取关键证据和子结论节点
  从文章中找出直接支撑核心结论（或其子结论）的关键节点：
  - 证据节点：作者引用的事实、数据、可观察到的市场/行业现象
  - 子结论节点：作者明确将其作为独立论点提出的中间判断
  问：「这个节点是作者论证不可缺少的吗？」是 → 提取；否 → 跳过。
  节点数量目标：整体 5–10 个（含核心结论），超过 10 个请合并相近节点。

Step D — 建立有向边（逻辑方向，与文本顺序无关）
  边方向 = 逻辑依赖方向：前提 → 结论。
  【关键】：文章中结论可能先于依据出现，但边的方向仍然是「依据 → 结论」。
  示例：作者先说「HALO具有护城河」，后说「因为重资产建设门槛极高」
        → 边方向仍是：「重资产建设门槛极高」→「HALO具有护城河」
  只建实际存在的支撑关系，不补推测边。

请严格按以下 JSON 格式输出，不要输出任何其他内容：

```json
{{
  "is_relevant_content": true,
  "skip_reason": null,
  "claims": [
    {{"id": 0, "text": "原文陈述（≤120字，可合并同主题相邻句）", "summary": "≤15字摘要"}},
    {{"id": 1, "text": "原文陈述（≤120字）", "summary": "≤15字摘要"}}
  ],
  "edges": [
    {{"from_id": 0, "to_id": 1}}
  ]
}}
```
"""
