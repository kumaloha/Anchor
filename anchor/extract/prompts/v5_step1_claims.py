"""
v5 Step 1 — 论证骨架提取
===========================
目标：提取论证的骨架——作者用来支撑核心主张的关键证据，
      以及由证据推导出的主要结论，识别两者之间的有向支撑关系。

提取哲学：
  - 提取论证的「承重墙」，不提取装饰性表述
  - 粒度对标「人工大纲」而非「逐句拆解」
  - 一篇文章/帖子通常 5–10 个节点足够
  - 中间推导步骤通常不作为独立节点，但有一个重要例外：
    若证据层与结论/建议层之间存在逻辑跳空，且跳空处的中间结论
    对目标读者而言是不言而喻的（作者强烈暗示但未明说），则必须提取。

输出 JSON：
{
  "is_relevant_content": true,
  "skip_reason": null,
  "claims": [{"id": 0, "text": "...", "summary": "..."}],
  "edges": [{"from_id": 0, "to_id": 1}]
}
"""

from __future__ import annotations

SYSTEM = """\
你是一名专业的论证分析师。从文章中提取论证骨架：作者用来支撑核心主张的关键事实，以及由此推导的结论。

【语言要求】
所有输出字段（text、summary）必须使用中文，无论原文是何语言。
英文内容需翻译为中文再提取，不得直接引用英文原句。

【提取原则】

原则 1 — 「承重墙」测试：对每个结论分别判断
  问：「对于支撑这个结论而言，删掉这个节点，这个结论的论证还成立吗？」
  不成立 → 提取；成立 → 跳过。
  ⚠️ 注意：文章可能有多条并行的论证线（如：结论A 和 结论B 各自有不同的事实依据）。
  一个事实对全局论证看似"次要"，但对它直接支撑的那个结论可能是不可缺少的——必须提取。

原则 2 — 粒度：不同实体、不同逻辑步骤必须分开
  ❌ 错误合并：「X出现风险，导致Y流动性收紧」→ 一个节点（丢失了因果链）
  ✓ 正确拆分：节点A「X出现风险」+ 节点B「X是Y的主要资金来源」+ 节点C「Y流动性收紧」，建边 A→C、B→C
  规则：涉及不同主体的事实，一律拆成独立节点。

原则 3 — 「关系型事实」是独立节点
  以下类型的陈述必须单独提取为事实节点，不能合并进其他节点：
  · X 是 Y 的资金来源 / 融资渠道
  · X 依赖 Y / X 与 Y 深度关联
  · X 的结构与 Y 历史上相似
  这类连接型事实往往是论证链的枢纽，删掉后因果关系就断了。

原则 4 — 区分证据和结论，不合并
  可观察事实/数据 → 事实节点
  作者的判断/推断 → 结论节点
  边方向：事实 → 结论

原则 5 — 区分「推测补充」与「强烈暗示的中间结论」

  ✗ 禁止：依赖外部知识或提取者自身逻辑补充的陈述（作者没说、也没有充分证据在原文中）

  ✓ 必须提取：强烈暗示的中间结论
    判断标准（三条必须同时满足）：
    ① 删掉它，证据层与行动/结论层之间的论证链会断裂（承重墙测试）
    ② 它完全由原文已呈现的证据推导，不依赖任何外部知识
    ③ 目标读者（该领域专家）会一致同意：这是作者在表达的意思，只是没有明说

    【背景】部分作者因地位、受众或语境（如影响市场风险、监管敏感、
    写给专业读者）习惯用事实堆叠代替直接陈述，中间结论留给读者推导。
    这类"不言而喻"的中间结论是整篇论证的枢纽，必须显式提取出来。

  ⚠️ 特别注意：「目的/本质重定义」型陈述
    当作者以「X的目的是Y」「X的本质是Z」「真正的风险是...」等定义句形式出现时，
    不要将其当作背景陈述略过——这往往是整篇论证的枢纽结论，
    它重设了后续所有建议成立的前提，必须作为独立结论节点提取。

原则 5b — 「当前状态类结论」的 claim text 必须携带具体特征

  当结论描述的是「当前处于X状态/阶段」时，claim text 不能只写标签，
  必须列出作者在文中用来识别该状态的 2–3 个核心可观察特征。
  ✗ 错误：「当前处于大周期晚期」（只有标签，无从核查）
  ✓ 正确：「当前处于大周期晚期：主权债实际利率历史性为负、金融资产总值
            相对真实资产达历史极值、内部财富分化加剧、地缘紧张升级」

原则 6 — 节点数量：6–15 个
  · 短帖/单一论点：6–10 个
  · 长篇分析文章（>2000字，含框架介绍→历史证据→当前应用三段式）：可达 12–15 个
  宁可多拆一个节点，也不要把不同主体的事实合并。

原则 7 — 三类容易遗漏的关键声明，必须专门扫描：
  ① 「当前位置判断」：作者对现状/当前时点的诊断（如"目前处于大周期晚期""当前实际利率为历史最低"）
    → 这类陈述是框架落地于现实的核心结论，必须作为独立节点提取
  ② 「行动建议」：作者给出的具体投资/行为建议（含"应该持有/减配/分散/对冲"等）
    → 必须提取（最终会被归为 Solution 类节点）
  ③ 「前瞻预判」：作者对未来的明确预判（含"将会/必然/可以预期/如果X则Y"等）
    → 必须提取（最终会被归为 Prediction 类节点）

输出合法 JSON，不加任何其他文字。\
"""


def build_user_message(
    content: str, platform: str, author: str, today: str,
    author_intent: str | None = None,
) -> str:
    intent_line = f"\n作者意图（预判）：{author_intent}" if author_intent else ""
    return f"""\
## 分析上下文
当前日期：{today}
平台：{platform}
作者：{author}{intent_line}

## 文章内容
{content}

## 提取任务

Step A — 相关性判断
  此内容是否包含可分析的论证（观点、事实依据或预测）？
  若否（广告/表情包/纯感情内容），设 is_relevant_content=false，其余字段留空。

Step B — 识别核心结论
  找到作者最终想让读者接受的核心主张。
  注意：金融/评论类文章常见「先抛结论、再给依据」的顶层结构，核心结论可能出现在
  开头、结尾或任何位置（常以「笔者认为/综合来看/在这个意义上/更重要的是」引导）。

Step C — 提取关键证据和子结论节点
  从文章中找出直接支撑核心结论（或其子结论）的关键节点：
  - 证据节点：作者引用的事实、数据、可观察到的市场/行业现象
  - 子结论节点：作者明确将其作为独立论点提出的中间判断
  - 行动建议节点：作者给出的具体建议（含「应持有/减配/分散/对冲」等）
  - 前瞻预判节点：作者对未来走势或后果的明确预判（「将/必然/如果X则Y」）

  额外检查——「论证链跳空」扫描：
  完成初步提取后，检查每一条从证据指向结论/建议的路径：
  问：「这两个节点之间，有没有一步'显而易见但作者没说出口'的推断？」
  若有，且满足原则5的三个判断标准，则将其补充为独立节点。
  这类节点通常是作者框架中的"目标重定义"或"视角转换"——
  它承接上方的宏观诊断，使下方的行动建议逻辑上成立。

  节点数量目标：短帖 6–10 个；长篇分析文章（>2000字）可达 12–15 个。

Step D — 建立有向边（逻辑方向，与文本顺序无关）
  边方向 = 逻辑依赖方向：前提 → 结论。
  【关键】：文章中结论可能先于依据出现，但边的方向仍然是「依据 → 结论」。
  示例：作者先说「HALO具有护城河」，后说「因为重资产建设门槛极高」
        → 边方向仍是：「重资产建设门槛极高」→「HALO具有护城河」
  只建实际存在的支撑关系，不补推测边。

请严格按以下 JSON 格式输出，不要输出任何其他内容：

```json
{{
  "is_relevant_content": true,
  "skip_reason": null,
  "claims": [
    {{"id": 0, "text": "原文陈述（≤120字，可合并同主题相邻句）", "summary": "≤15字摘要"}},
    {{"id": 1, "text": "原文陈述（≤120字）", "summary": "≤15字摘要"}}
  ],
  "edges": [
    {{"from_id": 0, "to_id": 1}}
  ]
}}
```
"""
